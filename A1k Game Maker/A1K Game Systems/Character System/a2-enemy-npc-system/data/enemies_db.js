// Helper functions
export function getEnemyById(id) {
  return ENEMIES_DATABASE[id] || null;
}

export function getEnemiesByTier(tier) {
  return Object.values(ENEMIES_DATABASE).filter(e => e.tier === tier);
}

export function getEnemiesByElement(element) {
  return Object.values(ENEMIES_DATABASE).filter(e => e.element === element);
}

export function getBasicEnemies() {
  return getEnemiesByTier("C");
}

export function getEliteEnemies() {
  return getEnemiesByTier("B").concat(getEnemiesByTier("A"));
}

export function getMiniBosses() {
  return getEnemiesByTier("S");
}

export function getAllEnemies() {
  return Object.values(ENEMIES_DATABASE);
}

